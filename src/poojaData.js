export const getPoojasByMonth = (month) => {
  return poojaSchedule.filter(pooja => {
    const poojaMonth = pooja.date.split('-')[1];
    return poojaMonth === month.toString().padStart(2, '0');
  });
};

export const getPoojasByCategory = (category) => {
  return poojaSchedule.filter(pooja => pooja.category === category);
};

export const getSpecialPoojas = () => {
  return poojaSchedule.filter(pooja => pooja.special === true);
};

export const getAvailablePoojas = () => {
  return poojaSchedule.filter(pooja => pooja.available === true);
};

export const formatMalayalamDate = (date) => {
  const dateObj = new Date(date);
  const day = dateObj.getDate();
  const month = dateObj.getMonth() + 1;
  const monthName = malayalamMonths.find(m => m.number === month)?.malayalam;
  return `${monthName} ${day}`;
};

export const getMalayalamDay = (date) => {
  const dateObj = new Date(date);
  const dayIndex = dateObj.getDay();
  return malayalamDays[dayIndex]?.malayalam;
};

